function ArithmeticOperations(x,y){
    console.log("Arithmetic Operations")
    console.log(x+y)
    console.log(x-y)
    console.log(x*y)
    console.log(x/y)
    console.log(x%y)
    console.log(++x)
    console.log(x++)
    console.log(y--)
    console.log(--y)
}

function LogicalOperations(x,y){
    console.log("Logical Operations")
    console.log(x && y)
    console.log(x || y)
    console.log(!(x))
}


function ComparisonOperations(x,y){
    console.log("Comparison Operations")
    console.log(x == y)
    console.log(x != y)
    console.log(x > y)
    console.log(x >= y)
    console.log(x < y)
    console.log(x <= y)
    console.log(x === y)
    console.log(x !== y)
}



